/* Дано четырехзначное число. Определите, является ли его десятичная запись симметричной. 
Если число симметричное, то выведите 1, иначе выведите любое другое целое число. Число может 
иметь меньше четырех знаков, тогда нужно считать, что его десятичная запись 
дополняется слева незначащими нулями. */

#include <iostream>

using namespace std;

int main() {
   int n, n1, n2, n3, n4, s1, s2;
   cin >> n;
   n1 = n / 1000;
   n2 = n / 100 % 10;
   n3 = n / 10 - n1 * 100 - n2 * 10;
   n4 = n % 10;
   s1 = (n1 - n4) * (n1 - n4);
   s2 = (n2 - n3) * (n2 - n3);
   cout << s1 + s2 + 1 << endl;
   return 0;
}

